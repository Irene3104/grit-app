/**
 * 알에서 랜덤 부화 — 어떤 캐릭터가 나올지 모름!
 * localStorage에 저장해서 한 번 부화하면 고정
 */
export function hatchEgg(): CreatureType {
  const saved = localStorage.getItem('questify_creature');
  if (saved && saved in CREATURES) return saved as CreatureType;

  const types: CreatureType[] = ['dinosaur', 'dragon', 'slime', 'dog', 'cat'];
  const random = types[Math.floor(Math.random() * types.length)];
  localStorage.setItem('questify_creature', random);
  return random;
}

/**
 * 현재 캐릭터 조회 (부화 안 됐으면 null)
 */
export function getCreature(): CreatureType | null {
  const saved = localStorage.getItem('questify_creature');
  return saved && saved in CREATURES ? saved as CreatureType : null;
}

/**
 * 레벨 기반 진화 단계 인덱스 (0~3)
 */
export function getEvolutionIndex(level: number): number {
  if (level >= 10) return 3;
  if (level >= 6) return 2;
  if (level >= 3) return 1;
  return 0;
}

/**
 * 다음 진화 레벨 (null이면 최종)
 */
export function getNextEvolutionLevel(level: number): number | null {
  if (level >= 10) return null;
  if (level >= 6) return 10;
  if (level >= 3) return 6;
  return 3;
}
